<div class='sectionContent'>
<div class='sectionTitle'>Our Mission</div>
Rhodesian Ridgeback Rescue, Inc., (RRRInc) is a not-for-profit, charitable organization dedicated to the rescue, care, and adoption of abandoned or unwanted Rhodesian Ridgeback dogs, as well as to protecting and preserving the integrity and welfare of the Rhodesian Ridgeback breed.
</div>

<div class='sectionContent'>
<div>Proud to be...</div>
<div class='allVolunteerOrg'>An All-Volunteer Organization!</div>
</div>

<div class='sectionContent'>
RRRInc is an all-volunteer organization that includes breeders, owners, and other devotees of the breed. RRRInc was established under Section <span class='no_break'>501(c)(3)</span> of the United States Internal Revenue Code.
</div>

<div class='sectionContent'>
Rhodesian Ridgeback Rescue, Inc., is the official national rescue program of the <a href='http://www.akc.org/' target='_blank'>AKC</a>-recognized US national breed club, the <a href='http://www.rrcus.org/' target='_blank'>Rhodesian Ridgeback Club of the United States, Inc., (RRCUS)</a>, and the <a href='http://www.ckc.ca/' target='_blank'>CKC</a>-recognized Canadian national breed club, the <a href='http://www.rrclubofcanada.org/' target='_blank'>Rhodesian Ridgeback Club of Canada (RRCC)</a>. RRRInc is also the official rescue program for most local and regional Rhodesian Ridgeback clubs across the USA and Canada.
</div>

<div class='sectionContent'>
Rhodesian Ridgeback Rescue, Inc., is comprised of a network of <a href='coordinators.html' target='_top'>volunteers</a> across the USA and Canada, and operates under an established set of policies, procedures, and guidelines. RRRInc volunteers endeavour to assist Ridgeback owners in raising happy healthy dogs by providing information on the breed, and assisting with training and care throughout a dog's life.
</div>

<div class='sectionContent'>
<div id='history' class='sectionTitle'>Our History</div>
In 2005, Rhodesian Ridgeback Rescue, Inc., celebrated 35 years of helping Rhodesian Ridgebacks in need! From its beginnings in 1970 as the adoption committee of the Rhodesian Ridgeback Club of the United States, Inc., (RRCUS) until today, RRRInc has evolved into a responsible and efficient, while at the same time compassionate, organization capable of rescuing and providing care for hundreds of Rhodesian Ridgebacks every year across the USA and Canada. RRRInc was spun off from RRCUS in 2000 as a separate corporation with separate officers and directors, but has remained to this day RRCUS's official rescue program. RRRInc obtained its status as an IRS 501(c)(3) charitable organization at the time of incorporation, which allowed for donations to be tax deductible for the first time.
</div>

<div class='sectionContent'>
Since 2000, RRRInc has greatly expanded its volunteer base. It has also successfully reached out to the worldwide community of Rhodesian Ridgeback owners who, through their never-ending generosity and love for the breed, provide regular, annual financial support to Rhodesian Ridgeback Rescue, Inc. RRRInchas also since become the official rescue program for the Rhodesian Ridgeback Club of Canada, as well as most local and regional Rhodesian Ridgeback clubs. Rather than each club operating its own rescue program, they now take advantage of the efficiencies, experience, and resources of one national program. This has allowed RRRInc to grow larger and more capable of caring for all rescued dogs, but especially those with special needs, such as expensive surgery and rehabilitation. RRRInc's goal is to provide care for all rescued Rhodesian Ridgebacks in need. RRRInc does not turn away any dogs because it cannot afford them. Since 2000, RRRInc has accepted more than 4,000 Ridgebacks into its rescue program.
</div>

<div class='sectionContent'>
<div id='msaada' class='sectionTitle'>The Msaada Ridgebacks</div>
<span class='msaada'>Msaada</span> is the name used by Rhodesian Ridgeback Rescue, Inc., for its family of rescued dogs. <span class='msaada'>Msaada</span> means &quot;aid, assistance, help, or support&quot; in the Swahili language.
</div>

<div class='sectionContent'>
<div id='volunteer' class='sectionTitle'>Volunteer with Us!</div>
If you are interested in volunteering with RRRInc, there are many different opportunities available. They include certain State, Provincial, and Area Rescue Coordinator positions; fostering and transporting dogs; talking with adoption applicants, animal shelters and others about the breed; fundraising; and administrative support tasks, such as bookkeeping and record-keeping. If you are interested in any of these or other volunteer opportunities, then please visit our <a href='how_you_can_help.html' target='_top'>How You Can Help!</a> page and submit an online Volunteer Application today!
</div>

